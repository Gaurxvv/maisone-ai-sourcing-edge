const REGIONS: Region[] = ["Japan", "United Kingdom", "Europe", "United States", "India", "China"];
const SEASONS = ["S/S 26", "F/W 26", "S/S 27"];
export function TrendForecast() {
  const { t } = useLanguage();
  const [region, setRegion] = useState<Region>("Japan");
  const [forecast, setForecast] = useState<Forecast | null>(null);
  const [loading, setLoading] = useState<boolean>(true);

  const fetchForecast = async () => {
    setLoading(true);
    try {
      const { data, error } = await supabase
        .from("trends")
        .select("fabrics, colors, silhouettes")
        .eq("region", region)
        .order("updated_at", { ascending: false })
        .limit(1)
        .maybeSingle();

      if (error) {
        console.warn("Could not query 'trends' database table:", error.message);
        setForecast(DEFAULT_DATA[region] || null);
      } else if (data) {
        setForecast({
          fabrics: typeof data.fabrics === "string" ? JSON.parse(data.fabrics) : data.fabrics,
          colors: typeof data.colors === "string" ? JSON.parse(data.colors) : data.colors,
          silhouettes: typeof data.silhouettes === "string" ? JSON.parse(data.silhouettes) : data.silhouettes,
        });
      } else {
        setForecast(DEFAULT_DATA[region] || null);
      }
    } catch (err) {
      console.warn("Failed to load forecast:", err);
      setForecast(DEFAULT_DATA[region] || null);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    fetchForecast();
  }, [region]);

  return (
    <section id="trends" className="relative py-32">
      <div className="mx-auto max-w-7xl px-6">
        <div className="flex flex-col lg:flex-row lg:items-end lg:justify-between gap-8 mb-12">
          <div className="max-w-2xl">
            <p className="text-[10px] uppercase tracking-[0.3em] text-electric mb-6">{t("trendForecast.label")}</p>
            <h2 className="font-serif text-4xl sm:text-6xl tracking-tight text-balance">
              {t("trendForecast.heading")} <span className="italic gradient-text">{t("trendForecast.headingHighlight")}</span>{t("trendForecast.headingEnd")}
            </h2>
            <p className="mt-6 text-muted-foreground">
              {t("trendForecast.description")}
            </p>
          </div>
          
          <div className="flex flex-wrap gap-1.5 p-1 glass rounded-full border border-border">
            {REGIONS.map((r) => (
              <button
                key={r}
                onClick={() => setRegion(r)}
                className={`text-xs px-3.5 py-1.5 rounded-full transition-colors cursor-pointer ${
                  region === r ? "bg-foreground text-background" : "text-muted-foreground hover:text-foreground"
                }`}
              >
                {r}
              </button>
            ))}
          </div>
        </div>

        {loading ? (
          <div className="h-[400px] flex items-center justify-center rounded-3xl border border-white/5 glass bg-white/[0.01]">
            <div className="flex flex-col items-center gap-3">
              <Loader2 className="size-8 animate-spin text-electric" />
              <p className="text-xs text-muted-foreground">{t("trendForecast.loadingText")}</p>
            </div>
          </div>
        ) : forecast ? (
          <div className="grid lg:grid-cols-3 gap-5">
            {/* Fabrics */}
            <Panel title={t("trendForecast.panelFabrics")} icon={Activity}>
              <AnimatePresence mode="wait">
                <motion.div
                  key={region + "-fabrics"}
                  initial={{ opacity: 0, y: 8 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -8 }}
                  transition={{ duration: 0.25 }}
                  className="space-y-3"
                >
                  {forecast.fabrics.map((f) => (
                    <div key={f.name} className="space-y-1.5">
                      <div className="flex items-center justify-between text-sm">
                        <span>{f.name}</span>
                        <span className="text-xs text-electric tabular-nums">{f.score}</span>
                      </div>
                      <div className="h-1.5 rounded-full bg-secondary overflow-hidden">
                        <motion.div
                          initial={{ width: 0 }}
                          animate={{ width: `${f.score}%` }}
                          transition={{ duration: 0.7, ease: "easeOut" }}
                          className="h-full rounded-full bg-gradient-to-r from-electric to-violet-glow"
                        />
                      </div>
                      <p className="text-[10px] text-muted-foreground">{f.signal}</p>
                    </div>
                  ))}
                </motion.div>
              </AnimatePresence>
            </Panel>

            {/* Colors */}
            <Panel title={t("trendForecast.panelColors")} icon={Sparkles}>
              <AnimatePresence mode="wait">
                <motion.div
                  key={region + "-colors"}
                  initial={{ opacity: 0, y: 8 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -8 }}
                  transition={{ duration: 0.25 }}
                  className="space-y-3"
                >
                  {forecast.colors.map((c) => (
                    <div key={c.name} className="flex items-center gap-3 rounded-xl p-2.5 glass border border-border">
                      <div
                        className="size-10 rounded-lg shrink-0 ring-1 ring-foreground/10"
                        style={{ backgroundColor: c.hex }}
                      />
                      <div className="flex-1 min-w-0">
                        <div className="flex items-center justify-between gap-2">
                          <p className="text-sm truncate">{c.name}</p>
                          <span className="text-xs text-electric tabular-nums">{c.score}</span>
                        </div>
                        <p className="text-[10px] text-muted-foreground">
                          Pantone {c.pantone} · {c.hex.toUpperCase()}
                        </p>
                      </div>
                    </div>
                  ))}
                </motion.div>
              </AnimatePresence>
            </Panel>

            {/* Silhouettes */}
            <Panel title={t("trendForecast.panelSilhouettes")} icon={TrendingUp}>
              <AnimatePresence mode="wait">
                <motion.div
                  key={region + "-silhouettes"}
                  initial={{ opacity: 0, y: 8 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -8 }}
                  transition={{ duration: 0.25 }}
                  className="space-y-3"
                >
                  {forecast.silhouettes.map((s) => (
                    <div key={s.name} className="rounded-xl p-3 glass border border-border">
                      <div className="flex items-center justify-between mb-2">
                        <p className="text-sm">{s.name}</p>
                        <span className="text-xs text-electric tabular-nums">{s.score}</span>
                      </div>
                      <div className="h-1 rounded-full bg-secondary overflow-hidden mb-2">
                        <motion.div
                          initial={{ width: 0 }}
                          animate={{ width: `${s.score}%` }}
                          transition={{ duration: 0.7 }}
                          className="h-full rounded-full bg-gradient-to-r from-cyan-glow to-electric"
                        />
                      </div>
                      <p className="text-[10px] text-muted-foreground">{s.note}</p>
                    </div>
                  ))}

                  <div className="mt-4 pt-4 border-t border-border text-[10px] text-muted-foreground flex items-center justify-between">
                    <span>{t("trendForecast.signalsAnalysed")}</span>
                    <span className="tabular-nums text-foreground/80">2.4M / 6h</span>
                  </div>
                </motion.div>
              </AnimatePresence>
            </Panel>
          </div>
        ) : (
          <div className="text-center py-12 text-muted-foreground">{t("trendForecast.noForecast")}</div>
        )}
      </div>
    </section>
  );
}

