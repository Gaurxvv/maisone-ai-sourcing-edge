export type Region = "Japan" | "United Kingdom" | "Europe" | "United States" | "India" | "China";

export type Forecast = {
  fabrics: { name: string; score: number; signal: string }[];
  colors: { name: string; hex: string; pantone: string; score: number }[];
  silhouettes: { name: string; score: number; note: string }[];
};

export const generateTrendForecastFn = createServerFn({ method: "POST" })
  .validator((d: { region: string; season: string }) => d)
  .handler(async ({ data }) => {
    const { region, season } = data;
    const apiKey = process.env.KIMI_API_KEY;
    const baseURL = process.env.KIMI_BASE_URL || "https://api.moonshot.cn/v1";
    const model = process.env.KIMI_MODEL || "moonshot-v1-8k";

    if (!apiKey) {
      throw new Error("Missing KIMI_API_KEY environment variable.");
    }

    const systemPrompt = `You are a professional fashion trend forecasting AI. Generate a realistic, trend-accurate fashion forecast for the region: "${region}" and the season: "${season}".

You must return ONLY a raw JSON object with the following exact typescript structure:
{
  "fabrics": [
    { "name": "Fabric Name", "score": 90, "signal": "Reason/signal of trend" }
  ],
  "colors": [
    { "name": "Color Name", "hex": "#HEXCOLOR", "pantone": "Pantone code", "score": 85 }
  ],
  "silhouettes": [
    { "name": "Silhouette Name", "score": 80, "note": "Brief style/season note" }
  ]
}

Provide exactly 4 fabrics, 4 colors (with valid CSS hex codes starting with '#' and matching Pantone formats), and 3 silhouettes. Do not include any markdown styling like \`\`\`json or explanation text. Output pure valid JSON.`;

    const response = await fetch(`${baseURL}/chat/completions`, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Authorization": `Bearer ${apiKey}`,
      },
      body: JSON.stringify({
        model,
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: `Generate fashion forecast for region "${region}" and season "${season}".` },
        ],
        temperature: 0.7,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      throw new Error(`API call failed: ${errorText}`);
    }

    const result = await response.json();
    const content = result.choices?.[0]?.message?.content?.trim();
    if (!content) {
      throw new Error("No response content from AI model.");
    }

    let cleanJson = content;
    if (cleanJson.startsWith("```")) {
      cleanJson = cleanJson.replace(/^```json\s*/, "").replace(/```$/, "").trim();
    }

    try {
      const parsed = JSON.parse(cleanJson);
      if (!parsed.fabrics || !parsed.colors || !parsed.silhouettes) {
        throw new Error("Missing expected JSON properties.");
      }
      return parsed;
    } catch (e: any) {
      console.error("Failed to parse LLM JSON:", cleanJson);
      throw new Error(`Invalid JSON format generated by AI: ${e.message}`);
    }
  });

